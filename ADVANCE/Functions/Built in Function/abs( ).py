# In this lets see about the "Built-in functions" in python.

# Built-in function is defined as the functions whose functionality is pre-defined in the python compiler.

# Lets see about "abs( ) bilt-in function" in python.

# abs( ) - Used to return the absolute value of a number.

# abs( ) will be taking up only one argument , an argument can be of interger or floating- point number , for which the absolute value will be returned.

# Note : For complex number , abs( ) returns its magnitude.

# Here is the program for abs( )

integer = 10 # Positive integer Value

print ( "\nAbsolute value of 10 is : " , abs( integer ) )

integer = -10 # Negative integer Value

print ( "\nAbsolute value of -10 is : " , abs( integer ) )

FloatingPoint = 10.123 # positive floating point Value

print ( "\nAbsolute value of 10.123 is : " , abs( FloatingPoint ) )

FloatingPoint = -10.123 # Negative floating point Value

print ( "\nAbsolute value of -10.123 is : " , abs( FloatingPoint ) )