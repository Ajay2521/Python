# In this lets see about the "Built-in functions" in python.

# Built-in function is defined as the functions whose functionality is pre-defined in the python compiler.

# Lets see about "int( ) bilt-in function" in python.

# int( ) - Used to get an int values. 

# Note : int( ) is used to convert a float or string datatype into int datatype.
 
# Here is the program for int( ).

Int = int ( 10 ) # integer type.

print( "\nInteger Value is : " , Int )

FloatInt = int ( 100.123 ) # integer type.

print( "\nFloat to Integer Value is : " , FloatInt )

StringInt = int ( "20" ) # integer type.

print( "\nString to Integer Value is : " , StringInt )

