# In this lets see about the "Built-in functions" in python.

# Built-in function is defined as the functions whose functionality is pre-defined in the python compiler.

# Lets see about "ord( ) bilt-in function" in python.

# ord( ) - Used to return an unicode value for the given String.

# Here is the program for ord( ).

print( "\nUnicode value of \"5\" is : " , ord( '5' ) )

print( "\nUnicode value of \"R\" is : " , ord( 'R' ) )

print( "\nUnicode value of \"r\" is : " , ord( 'r' ) )

print( "\nUnicode value of \"@\" is : " , ord( '@' ) )

print( "\nUnicode value of \"#\" is : " , ord( '#' ) )


