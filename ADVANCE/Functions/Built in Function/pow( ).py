# In this lets see about the "Built-in functions" in python.

# Built-in function is defined as the functions whose functionality is pre-defined in the python compiler.

# Lets see about "pow( ) bilt-in function" in python.

# pow( ) - Used to compute the power of a number.

# Here is the program for pow( ).

print( "\n4 power 3 is : " , pow( 4 , 3 ) )

print( "\n-4 power 3 is : " , pow( -4 , 3 ) )

print( "\n4 power -3 is : " , pow( 4 , -3 ) )

print( "\n-4 power -3 is : " , pow( -4 , -3 ) )


