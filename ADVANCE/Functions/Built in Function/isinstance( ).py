# In this lets see about the "Built-in functions" in python.

# Built-in function is defined as the functions whose functionality is pre-defined in the python compiler.

# Lets see about "isinstance( ) bilt-in function" in python.

# isinstance( ) - Used to check whether the given object is an instance of that class.

# Here is the program for isinstance( ).

print( "\nIs \"5\" is an instance of Integer ?: " , isinstance( 5 , int ) )

print( "\nIs \"10.123\" is an instance of Integer ?: " , isinstance( 10.123 , int ) )

print( "\nIs \"5\" is an instance of Float ?: " , isinstance( 5 , float ) )

print( "\nIs \"10.123\" is an instance of Float ?: " , isinstance( 10.123 , float ) )

