# lets see about "date and time" in python.

# sleep() = Used to stop the execution for a given amount of time.

# Thus output will be delayed for the number of seconds provided.

# Here is the program for "sleep( )" in python.

import time

for i in range( 1, 6 ): # program is excuted for 5 times .

    print("\nThe delay to print the output from first output to this output is : " , i * 2 )

    time.sleep( 2 ) # time delay is 2 , Thus each statement will be printed with a delay of 2 sec.


