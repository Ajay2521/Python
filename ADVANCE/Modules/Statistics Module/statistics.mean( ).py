# Lets see about Statistics module in python

# statistics module provides the functions to mathematical statistics of numeric data.

# statistics.mean( ) = Used to calculate the mean value of a list or sequence of value.

# Here is the example program for statistics.mean( )

import statistics

List = [ 1, 3, 6, 5, 10, 15, 20, 30, 32, 28]

Mean = statistics.mean(List)

print('\nList value is :\n',List)

print('\nMean value of the List is :',Mean)

