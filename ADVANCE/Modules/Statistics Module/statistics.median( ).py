# Lets see about Statistics module in python

# statistics module provides the functions to mathematical statistics of numeric data.

# statistics.median( ) = Used to return a middle value from the list.

# Here is the example program for statistics.median( )

import statistics

List = [ 1, 3, 6, 5, 10, 15, 20, 30, 32]

Median = statistics.median(List)

print('\nList value is :\n',List)

print('\nMedian value of the List is :',Median)


