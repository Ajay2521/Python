# In this lets see about "Negative Slice in the String Datatype" in Python.

# Negative slicing is possible in the Strings.

# It starts from the rightmost character, which is indicated as -1 and so on.

Name = 'Maayon' # String inside single quotes.

print() # Print an empty line for readablity.

print( Name[ -1 ] ) # Print the rightmost characters.

print() # Print an empty line for readablity.

print( Name[ -2 ] ) # Print the second rightmost characters.

print() # Print an empty line for readablity.\

print( Name[ -3 : ] ) # Print the characters from [ -3 ] index.

print() # Print an empty line for readablity.\

print( Name[ -6 : ] ) # Print the characters from [ -6 ] index.

print() # Print an empty line for readablity.\

print( Name[ -5 : -1 ] ) # Print the characters from [ -5 ] index to [ -1 ] index.

print() # Print an empty line for readablity.\

print( "The Reverse of the String \"" , Name , "\" is : " , Name[ : : -1  ] ) # Print all the characters in "Reverse order".
