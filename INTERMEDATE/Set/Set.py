# In this lets see more about "Set Datatype" in Python.

# set is the collection of the unordered items.

# Elements in the set must be unique, immutable.

# Set removes the duplicate elements.

# Sets are mutable which means we can modify it after its creation.

# Set has no index attachement.

# Thus we cannot directly access any element of the set by the index.

# But set can be printed all together.

# The items in the Set are separated with the comma ( , ) and enclosed with the Curly brackets { }.

# Python also provides the set() method, which can be used to create the set by the passed sequence.

# Here is the program to "Create set" in python.

Set = { "Maayon" , 19, "Techgen" }

Set1 = { 19, "Techgen" , "Maayon" }

print ( "\nThe value stored in First Set is : " , Set )

print ( "\nChecking the datatype of the variable \"Set1\" : " , type(Set) )

