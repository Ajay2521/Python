# In this lets see more about "Dictionary datatype" in python.

# Dictionary is used to store the data in a key-value pair format.

# In short it giveout the value for specific key.

# Dictionary is an mutable data type.

# The dictionary is defined into element Keys and values.

# Keys must be a single element

# Value can be any type such as list, tuple, integer, etc.

# Dictionary is created by using key - value pairs which is to be enclosed within the "curly brackets { } ", each key is separated from its value by the colon (:).

# Here is the program for "Dictionary"

Details = { "Name" : "Maayon" , "Age" : 19 }

print( "\nThe Details is...\n\n" , Details )

print ( ) # Prints an empty line for Readability.

print ( "Datatype of \"Details\" is...\n\n" , type ( Details ) )

