# In this lets see more about "Adding Set Datatype" in Python.

# update ( ) is the function used to add some particular item to the set.

# update ( ) is used to add multiple element. 

# Here is the program to "Adding set using Update ( )" in python.

Set = set( [ "Maayon" , 19, "Techgen" ] )

print ( "\nThe value stored in Set is Before using update ( ) : " , Set )

Set.update ( [ "SetAdded" , "SetUpdated" ] ) 

print ( "\nThe value stored in Set is After using update ( ) : " , Set )


