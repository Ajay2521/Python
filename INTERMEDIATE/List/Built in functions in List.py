# In this lets see about "Built-in functions for list" in Python

# Majorly used Built in functions in list is :

# 1) cmp ( list_variable_1 , list_variable_2 ) - used to compare two variables of list datatype.

# 2) len ( list_variable ) - used to find the length of the list.

# 3) max ( list_variable ) - used to find the maximum value in the list.

# 4) min ( list_variable ) - used to find the minimum value in the list.

# 3) list ( seq ) - used to converts any sequence to the list.

#Note - cmp() is no more in use after the version of python 3 and above

# Here is the program for all Built in functions.

list = [  30, 40, 50, 10, 20 ]

print ( "\nThe Length of the list is : " , len ( list ) ) 

print ( "\nThe Maximum Value of the list is : " , max ( list ) ) 

print ( "\nThe Minimum Value of the list is : " , min ( list ) ) 
