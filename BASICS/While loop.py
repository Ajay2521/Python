#In this program lets see about "Loop" concept in Python.

#Loops are the once which is used to repeat certain code for certain number of times.

#Thus loops will be executed for number of times or till the condition gets fails.

#ADVANTAGES of LOOPS:

#1) Loop is used for code re-usability.

#2) By using loop writing the code again and again can be avoided.

#3) By using loop traversing the element in data structures like array or linked list can be achived.

#There are 3 types of loop they are :

#1) for loop

#2) while loop

#3) do - while loop

#In this lets see about "while loop"

#while loop is used to repeat the statement or code for certian number of time.

#while loop will be executed until the given condition fails.

#while loop is also known as pre - tested loop or entry check loop.

#synatx for "while loop" is

#while expression :

#	statement(s)

#	increment

#While loop program example :

i = 1

print()

while ( i <= 10 ) : #While loop will be executed until condition becomes false.

	print(i)

	i = i + 1
