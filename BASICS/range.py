#In this lets see about "for loop with range()"

#The range() function is used to generate the sequence of the numbers.

#eg : if we declare range(10) it will generate the sequence from 0 to 9.

#synatx for "range()" is

#range( start , stop , step size )

#start - used represent the beginning number of the iteration.

#stop - used to represent the loop will end by stop - 1 . eg : range ( 1 , 10 ) then it will generate number from 1 to 9 . Stop is an Optional one.

#step size - used to skip the iteration for specific number. Step Size is an Optional one. By defalut the step size is 1.

#for loop program using range

