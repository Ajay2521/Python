#Lets see about "Assignment Operators" in Python Program.

#Majorly used Assignment Operation is :

#1) Assign  ( = )

#2) Assign and Addition ( += )

#3) Assign and Subtraction (  -= )

#4) Assign and Multiplication (  *= )

#5) Assign and Division ( /= )

#6) Assign and Reminder ( %= )

#7) Assign and Floor Division ( //= )

#Here is the Python Program for Assignment Operators

#Declearing Variable 'a = 10' and creating a Variable 'c = 0'  

a = 10

c = 0

# =(ASSIGN TO) Operator Example.

c = a # which is c = a 

print("\n = Operator Example, Value of c = " , c )

# += ( ADD And Assignment Operation ) Operator Example.

c += a # which is c = c + a 

print("\n += ( ADD And Assignment Operation ) Operator Example, Value of c = " , c )

# -= ( SUB And Assignment Operation ) Operator Example.

c -= a # which is c = c - a 

print("\n -= ( SUB And Assignment Operation ) Operator Example, Value of c = " , c )

# *= ( MULTI And Assignment Operation ) Operator Example.

c *= a # which is c = c * a 

print("\n *= ( MULTI And Assignment Operation ) Operator Example, Value of c = " , c )

# /= ( DIVISION And Assignment Operation ) Operator Example.

c /= a # which is c = c / a 

print("\n /= ( DIVISION And Assignment Operation ) Operator Example, Value of c = " , c )

# %= ( MODULUS And Assignment Operation ) Operator Example.

c = 11

c %= a # which is c = c % a 

print("\n %%= ( MODULUS And Assignment Operation ) Operator Example, Value of c = ", c )

# **= ( EXPONENT and Assignment Operation ) Operator Example.

c **= a # which is c = c ** a 

print("\n **= ( EXPONENT and Assignment Operation ) Operator Example, Value of c = ", c )

# //= ( FLOOR DIVISION and Assignment Operation ) Operator Example.

c //= a # which is c = c // a 

print("\n //= ( FLOOR DIVISION and Assignment Operation ) Operator Example, Value of c = ", c )
