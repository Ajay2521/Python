#In this program lets see about "if else Statement" in Python

#if else statement is an Decision Making Statement which is used to check for Condition.

#If the condition is true , then the code inside if block will be executed.

#else If the condition is false , then the code inside else block will be executed.

#Syntax for "if else statement" is

#if( Conditional Expression ) :

#	Statement 1

#	Statement 2

#	Statement N

#else :

#	Statement 1

#	Statement 2

#	Statement N

#Note : Since Python doesn't denote curly braces . Therefore "indentation" will play an imporatant role is makeing a block of code.

