#Program for if else statement

#Declaring Variable a = 10

a = 10

if ( a > 10 ) :

	print("\nFirst if Block is executed when a = 10, where \"a\" is Greater than 10.")

else :

	print("\nFirst else block is executed when a = 10, where \"a\" is Not Greater than 10.")

#Redeclaring Variable a = 20

